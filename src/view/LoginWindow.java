            String email = inpEmail.getText();
            String password = inpPassword.getText();
            EmployeeController empController = new EmployeeController();
            boolean respond = empController.authenticarUsuario(email, password);
            System.out.println("respuesta authenticación =>  "+ respond);
            if(respond){
                new MainWindow().setVisible(respond);
            }else{
                javax.swing.JOptionPane.showMessageDialog(this,"Las credenciales no son correctas");
            }

